package com.seeyon.v3x.peoplerelate.exception;

public class RelateMemberException extends Exception {

	private static final long serialVersionUID = 1L;

	public RelateMemberException() {
		super();
	}

	public RelateMemberException(String message, Throwable cause) {
		super(message, cause);
	}

	public RelateMemberException(String message) {
		super(message);
	}

	public RelateMemberException(Throwable cause) {
		super(cause);
	}
	
}
